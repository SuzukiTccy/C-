#include <iostream>
using namespace std;


/*=======================C++运算符，&&和||的巧用========================*/
int add(int c){
    int a = 0;
    c&&(a = add(c-1));
    cout << c+a << endl; //递归循环，直到传入c的值为0则结束循环
    //(!c)||(a=add(c-1)); //递归循环，直到传入c的值为0，(!c)就为真，结束循环

    return c+a;
}

int main(){
/*=======================C++运算符，&&和||的巧用========================*/
    add(10);


}

